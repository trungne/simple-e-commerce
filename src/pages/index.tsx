import {
  AppShell,
  Box,
  Divider,
  Flex,
  Header,
  Navbar,
  ScrollArea,
  Space,
} from "@mantine/core";
import { Card, Image, Text, Button, Group } from "@mantine/core";
import Head from "next/head";
import { useState } from "react";
import { api } from "~/utils/api";

export default function Home() {
  const [categoryFilter, setCategoryFilter] = useState<string | undefined>(
    undefined
  );

  const product = api.product.productList.useQuery({
    category: categoryFilter,
  });
  const category = api.product.categoryList.useQuery();

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center">
        <AppShell
          padding="md"
          navbar={
            <Navbar width={{ base: 220 }} p="xs">
              <Navbar.Section mt="xs">{/* Header with logo */}</Navbar.Section>

              <Box>
                <Text
                  align="center"
                  fw={700}
                  fz="xl"
                  variant="gradient"
                  gradient={{ from: "green", to: "blue", deg: 45 }}
                >
                  Categories
                </Text>
                <Space h="md"></Space>
              </Box>
              <Divider />
              <Navbar.Section grow component={ScrollArea} mx="-xs" px="xs">
                {/* Navbar content */}
                <Button.Group orientation="vertical">
                  {category.data?.map((category, idx) => {
                    const isSelected = categoryFilter === category.name;

                    return (
                      <Button
                        onClick={() => {
                          setCategoryFilter(category.name);
                        }}
                        
                        key={idx}
                        variant="subtle"
                        color={isSelected ? "blue" : "dark"}
                      >
                        {category.name}
                      </Button>
                    );
                  })}
                </Button.Group>
              </Navbar.Section>

              <Navbar.Section>{/* Footer with user */}</Navbar.Section>
            </Navbar>
          }
          header={
            <Header height={60} p="xs">
              {/* Header content */}
            </Header>
          }
          styles={(theme) => ({
            main: {
              backgroundColor:
                theme.colorScheme === "dark"
                  ? theme.colors.dark[8]
                  : theme.colors.gray[0],
            },
          })}
        >
          {/* Body */}
          <Flex
            mih={50}
            gap="md"
            justify="center"
            align="flex-start"
            direction="row"
            wrap="wrap"
          >
            {product.data?.map((product) => {
              return (
                <div key={product.id}>
                  <Card
                    style={{ width: 180, height: 250 }}
                    shadow="sm"
                    padding="sm"
                    withBorder
                  >
                    <Card.Section>
                      <Image src={product.image} height={100} alt="Image" />
                    </Card.Section>

                    <Group position="apart" mt="md" mb="xs">
                      <Text size="sm" weight={500} truncate>
                        {product.name}
                      </Text>
                    </Group>

                    <Text size="xs" color="dimmed" truncate>
                      {product.description}
                    </Text>

                    <Button
                      variant="light"
                      color="blue"
                      fullWidth
                      mt="lg"
                      radius="md"
                    >
                      Buy now
                    </Button>
                  </Card>
                </div>
              );
            })}
          </Flex>
        </AppShell>
      </main>
    </>
  );
}
