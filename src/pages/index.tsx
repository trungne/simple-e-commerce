import {
  AppShell,
  Box,
  Dialog,
  Divider,
  Flex,
  Header,
  Navbar,
  Pagination,
  ScrollArea,
  Space,
} from "@mantine/core";
import { Card, Image, Text, Button, Group } from "@mantine/core";

import Head from "next/head";
import { useState } from "react";
import type { Product } from "~/types/product";
import { api } from "~/utils/api";

export default function Home() {
  const [categoryFilter, setCategoryFilter] = useState<string | undefined>(
    undefined
  );

  const [page, setPage] = useState(0);

  const { data: productListResponse } = api.product.productList.useQuery({
    category: categoryFilter,
    page,
    perPage: 12,
  });
  const category = api.product.categoryList.useQuery();

  const [productToShowInDialog, setProductToShowInDialog] = useState<Product>();


  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center">
        <AppShell
          padding="md"
          navbar={
            <Navbar width={{ base: 220 }} p="xs">
              <Navbar.Section mt="xs">{/* Header with logo */}</Navbar.Section>

              <Box>
                <Text
                  align="center"
                  fw={700}
                  fz="xl"
                  variant="gradient"
                  gradient={{ from: "green", to: "blue", deg: 45 }}
                >
                  Categories
                </Text>
                <Space h="md"></Space>
              </Box>
              <Divider />
              <Navbar.Section grow component={ScrollArea} mx="-xs" px="xs">
                {/* Navbar content */}
                <Button.Group orientation="vertical">
                  {category.data?.map((category, idx) => {
                    const isSelected = categoryFilter === category.name;

                    return (
                      <Button
                        onClick={() => {
                          setCategoryFilter(category.name);
                        }}
                        key={idx}
                        variant="subtle"
                        color={isSelected ? "blue" : "dark"}
                      >
                        {category.name}
                      </Button>
                    );
                  })}
                </Button.Group>
              </Navbar.Section>

              <Navbar.Section>{/* Footer with user */}</Navbar.Section>
            </Navbar>
          }
          header={
            <Header height={60} p="xs">
              {/* Header content */}
            </Header>
          }
          styles={(theme) => ({
            main: {
              backgroundColor:
                theme.colorScheme === "dark"
                  ? theme.colors.dark[8]
                  : theme.colors.gray[0],
            },
          })}
        >
          {/* Body */}
          <Flex
            mih={50}
            gap="md"
            justify="center"
            align="flex-start"
            direction="row"
            wrap="wrap"
          >
            {productListResponse?.data.map((product) => {
              return (
                <Card
                  onClick={() => {
                    setProductToShowInDialog(product);
                  }}
                  key={product.id}
                  style={{ height: 250, flexBasis: "30%" }}
                  shadow="sm"
                  padding="sm"
                  withBorder
                >
                  <Card.Section>
                    <Image src={product.image} height={100} alt="Image" />
                  </Card.Section>

                  <Group position="apart" mt="md" mb="xs">
                    <Text size="sm" weight={500} truncate>
                      {product.name}
                    </Text>
                  </Group>

                  <Text size="xs" color="dimmed" truncate>
                    {product.description}
                  </Text>

                  <Button
                    variant="light"
                    color="blue"
                    fullWidth
                    mt="lg"
                    radius="md"
                  >
                    Buy now
                  </Button>
                </Card>
              );
            })}
          </Flex>
          <Flex className="mt-auto" justify="center">
            <Pagination
              value={page + 1}
              onChange={(page) => {
                setPage(page - 1);
              }}
              total={productListResponse?.totalPage ?? 0}
            />
          </Flex>
          <Dialog
            opened={!!productToShowInDialog}
            withCloseButton
            onClose={() => {
              setProductToShowInDialog(undefined);
            }}
          >
            <Text size="sm" mb="xs" weight={500}>
              Name: {productToShowInDialog?.name}
            </Text>
            <Text size="sm" mb="xs" weight={500}>
              Price: {productToShowInDialog?.price}
            </Text>
            <Text size="sm" mb="xs" weight={500}>
              Quantity: {productToShowInDialog?.quantity}
            </Text>
            <Text size="sm" mb="xs" weight={500}>
              Category: {productToShowInDialog?.category}
            </Text>
          </Dialog>
        </AppShell>
      </main>
    </>
  );
}
